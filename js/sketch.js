//the setup function runs once at the start
function setup() {
    var canvas = createCanvas(600, 600);
    canvas.parent("p5container");
}
//the draw function is a constant loop and runs 60 times a second
function draw() {
    background(220); //greyscale colour for background

    fill(255, 255, 255);
    ellipse(300, 300, 400);
    
    fill(167,217,212);
    square (150,150,300)
    
  
}

